package com.qingyun.mvpretrofitrx.mvp.entity;

public class InvestLog {
    private String id;
    private String gamename;
    private String gameuser;
    private String gamenumber;
    private String moneystate;
    private String money;
    private String time;
    private String color;
    private String state;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getGamename() {
        return gamename;
    }

    public void setGamename(String gamename) {
        this.gamename = gamename;
    }

    public String getGameuser() {
        return gameuser;
    }

    public void setGameuser(String gameuser) {
        this.gameuser = gameuser;
    }

    public String getGamenumber() {
        return gamenumber;
    }

    public void setGamenumber(String gamenumber) {
        this.gamenumber = gamenumber;
    }

    public String getMoneystate() {
        return moneystate;
    }

    public void setMoneystate(String moneystate) {
        this.moneystate = moneystate;
    }

    public String getMoney() {
        return money;
    }

    public void setMoney(String money) {
        this.money = money;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }


    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }
}
