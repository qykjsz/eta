package com.qingyun.mvpretrofitrx.mvp.entity;

public class GasPrice {

    private String name;
    private String gasmin;
    private String gasmax;
    private String gweimin;
    private String gweimax;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getGasmin() {
        return gasmin;
    }

    public void setGasmin(String gasmin) {
        this.gasmin = gasmin;
    }

    public String getGasmax() {
        return gasmax;
    }

    public void setGasmax(String gasmax) {
        this.gasmax = gasmax;
    }

    public String getGweimin() {
        return gweimin;
    }

    public void setGweimin(String gweimin) {
        this.gweimin = gweimin;
    }

    public String getGweimax() {
        return gweimax;
    }

    public void setGweimax(String gweimax) {
        this.gweimax = gweimax;
    }
}
