package com.qingyun.mvpretrofitrx.mvp.entity;

public class BusinessPayLog {
    private String from;
    private String money;
    private String moneyname;
    private String fimoney;
    private String fimoneyname;
    private String type;
    private String color;
    private String time;

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getFrom() {
        return from;
    }

    public void setFrom(String from) {
        this.from = from;
    }

    public String getMoney() {
        return money;
    }

    public void setMoney(String money) {
        this.money = money;
    }

    public String getMoneyname() {
        return moneyname;
    }

    public void setMoneyname(String moneyname) {
        this.moneyname = moneyname;
    }

    public String getFimoney() {
        return fimoney;
    }

    public void setFimoney(String fimoney) {
        this.fimoney = fimoney;
    }

    public String getFimoneyname() {
        return fimoneyname;
    }

    public void setFimoneyname(String fimoneyname) {
        this.fimoneyname = fimoneyname;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
